    private void btnFineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFineActionPerformed
        // TODO add your handling code here:
        JFrame f = new JFrame();
        String id = txtUserID.getText();
        String fine = txtFine.getText();
        if ( id.trim().length() == 0 || fine.trim().length() == 0)
        {
            JOptionPane.showMessageDialog(f,"Improper input format","Error",JOptionPane.WARNING_MESSAGE);
        }

        else
        {
            Session session = factory.openSession();
            Transaction tx = null;

            try {
                tx = session.beginTransaction();
                Customers c = (Customers)session.get(Customers.class, Integer.valueOf(id));
                if(c!=null)
                {
                    int p = JOptionPane.showConfirmDialog(null, "Would you like to update the fine of User ID "+id+" to Rs. "+ fine+"?","Fine Update",JOptionPane.YES_NO_OPTION);
                    if(p==0)
                    {
                        c.setFine(Integer.valueOf(fine));
                        session.update(c);
                        tx.commit();
                        JOptionPane.showMessageDialog(null,"Fine has been updated successfully");
                        listUsers();
                    }
                }
                else{
                    f = new JFrame();
                    JOptionPane.showMessageDialog(f,"No user with requested id found","Error",JOptionPane.WARNING_MESSAGE);
                }

            } catch (HibernateException e) {
                if (tx!=null) tx.rollback();
                e.printStackTrace();
            } finally {
                session.close();
            }
        }
    }//GEN-LAST:event_btnFineActionPerformed

