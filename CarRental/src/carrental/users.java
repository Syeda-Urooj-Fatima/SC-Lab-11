    public void listUsers()
    {
        Session session = factory.openSession();
        Transaction tx = null;
      
        try {
            tx = session.beginTransaction();
            List list = session.createQuery("FROM Customers").list(); 
            Vector<String> tableHeaders = new Vector<String>();
            Vector tableData = new Vector();
            tableHeaders.add("ID"); 
            tableHeaders.add("Name");
            tableHeaders.add("Contact number");
            tableHeaders.add("Email");
            tableHeaders.add("Address");
            tableHeaders.add("Fine");

            for(Object o : list) {
                Customers c = (Customers)o;
                Vector<Object> oneRow = new Vector<Object>();
                oneRow.add(c.getId());
                oneRow.add(c.getName());
                oneRow.add(c.getContactNo());
                oneRow.add(c.getEmail());
                oneRow.add(c.getAddress());
                oneRow.add(c.getFine());
                tableData.add(oneRow);
            }
            
            tblUsers.setModel(new DefaultTableModel(tableData, tableHeaders));
            tblUsers.getColumnModel().getColumn(0).setMaxWidth(35);
            tblUsers.getColumnModel().getColumn(1).setMaxWidth(110);
            tblUsers.getColumnModel().getColumn(2).setMaxWidth(100);
            tblUsers.getColumnModel().getColumn(3).setMaxWidth(160);
            tblUsers.getColumnModel().getColumn(4).setMaxWidth(400);
            tblUsers.getColumnModel().getColumn(5).setMaxWidth(50);
            
            tx.commit();
         } catch (HibernateException e) {
            if (tx!=null) tx.rollback();
            e.printStackTrace(); 
         } finally {
            session.close(); 
         }
    }
